package com.firstproject;

public class JavaString {
    public static void main(String[] args) {
        String fName = "Swornim";
        String lName = "Pangeni";
        String fullName = fName+" "+lName;
        System.out.println(fullName);
        System.out.println(fullName.length());
        System.out.println(fName.charAt(0));
        System.out.println(fName.replace('o', 'a'));
        System.out.println((fName.substring(3,5)));

        //Typecasting -- changing the variable type from one to another

    }
}
