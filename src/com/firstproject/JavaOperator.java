package com.firstproject;

public class JavaOperator {
    public static void main(String[] args) {
        int tax = 13;
        int price = 100;
        System.out.println(price + tax);
        System.out.println(price-tax);
        System.out.println(price/tax);
        System.out.println(price * tax);

        int num = 20;
        System.out.println(++num);

    }
}
